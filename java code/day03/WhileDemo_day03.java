package day03;
public class WhileDemo_day03 {
    public static void main(String[] args)
    {
        /*
        循环结构。循环语句什么时候用呢？
        当对某些语句需要执行很多次时，就使用循环结构。
        */
       int x=1;
       while(x<3)
       {
        System.out.println("x="+x);
        ++x;
       }
       System.out.println("Hello World!");
    }
}
