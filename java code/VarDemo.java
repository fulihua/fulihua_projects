public class VarDemo 
{
 public static void main(String[] args)
 {
    //数据类型  变量名 = 初始化值；
    byte b = 6;
    short s = 4;
    int i = 7;
    long l = 12345678900l;//对于过大的数，加l;
    float f = 2.3f;//小数后加f可以表示单精度存储；
    double d = 3.4;
    char c ='a';
    boolean bo = true;//bo = false;
    System.out.println("Hello World");

}
}

